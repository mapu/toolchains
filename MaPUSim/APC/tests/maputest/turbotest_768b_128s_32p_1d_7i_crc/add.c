void bit2byte(unsigned char *byte_stream, int *bit_stream,
	      unsigned int bit_len) {
  unsigned int i;
  unsigned char tmp = 0x0;
  for (i = 1; i <= bit_len; i++) {
    if (i % 8 != 0)
      tmp |= (*(bit_stream + i - 1) & 0x1) << (8 - i % 8);
    else {
      *(byte_stream + i / 8 - 1) = tmp | (*(bit_stream + i - 1) & 0x1);
      tmp = 0x0;
      }
  }
}
